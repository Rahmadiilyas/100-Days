package day.pkg100;

public class BelajarArray {
    public static void main(String[] args) {
        int [] a = {4,7,9,10,11};
        int jml = a.length;
        
        for (int i = 0; i<jml; i++){
            System.out.print(a[i]+ " ");
        }
        System.out.println("");
    }
  
}
